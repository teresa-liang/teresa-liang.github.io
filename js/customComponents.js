class CourseItem extends HTMLElement {
    constructor() {
        super();
    }

    connectedCallback() {
        var currentStatus =  this.getAttribute("current-status");
        if (currentStatus == "true"){
            var currentCourseStyling = "class='current-course'"
        } else {
            var currentCourseStyling = ""
        }

        this.innerHTML = `
            <li>
                <flower-bullet></flower-bullet>
                <div ${currentCourseStyling}>${this.getAttribute("course-name")}</div>
            </li>
        `;
    }
}

